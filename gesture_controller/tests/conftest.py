failed_reports = []
collected_warnings = []


def pytest_runtest_logreport(report) -> None:
    """Collect failed test reports."""
    if report.when in ("setup", "call", "teardown"):
        if report.failed:
            failed_reports.append(report)


def pytest_warning_recorded(warning_message, when, nodeid, location) -> None:
    """Collect warnings recorded during test runs."""
    collected_warnings.append(
        {
            "message": str(warning_message.message),
            "category": (
                warning_message.category.__name__ if warning_message.category else "Warning"
            ),
            "filename": warning_message.filename,
            "lineno": warning_message.lineno,
            "when": when,
            "nodeid": nodeid,
        }
    )


