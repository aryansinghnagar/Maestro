def pytest_unconfigure(config) -> None:
    """Generate the structured error_log.md file at the end of the test run (Sprint 4 Sidestep)."""
    from pathlib import Path

    log_path = Path(config.rootdir) / "error_log.md"

    try:
        with open(log_path, "w", encoding="utf-8") as f:
            f.write("# Maestro Test Execution Error & Warning Log\n\n")
            f.write(
                "This file is automatically generated by pytest hooks to assist coding agents in iterative improvement.\n\n"
            )

            if not failed_reports and not collected_warnings:
                f.write("## ✅ All Tests Passed\n\n")
                f.write(
                    "No failures, errors, or warnings were encountered during this test execution run.\n"
                )
                return

            if failed_reports:
                f.write("## ❌ Test Failures\n\n")
                for report in failed_reports:
                    f.write(f"### Test: `{report.nodeid}`\n")
                    f.write(f"- **Outcome**: {report.outcome}\n")
                    f.write(f"- **Phase**: {report.when}\n\n")
                    f.write("#### Traceback & Error Details:\n")
                    f.write("```text\n")
                    f.write(str(report.longrepr) + "\n")
                    f.write("```\n\n")

                    if hasattr(report, "capstdout") and report.capstdout:
                        f.write("#### Captured Stdout:\n")
                        f.write("```text\n")
                        f.write(report.capstdout + "\n")
                        f.write("```\n\n")
                    if hasattr(report, "capstderr") and report.capstderr:
                        f.write("#### Captured Stderr:\n")
                        f.write("```text\n")
                        f.write(report.capstderr + "\n")
                        f.write("```\n\n")
                    f.write("---\n\n")

            if collected_warnings:
                f.write("## ⚠️ Pytest Warnings\n\n")
                for w in collected_warnings:
                    f.write(f"- **Category**: `{w['category']}`\n")
                    f.write(f"  - **Message**: {w['message']}\n")
                    f.write(f"  - **Location**: `{w['filename']}:{w['lineno']}`\n")
                    if w["nodeid"]:
                        f.write(f"  - **Node ID**: `{w['nodeid']}`\n")
                    f.write("\n")
    except Exception as e:
        print(f"Failed to generate error_log.md: {e}")


def make_hand(landmarks: list[tuple[float, float, float]], handedness: str = "Right") -> Hand:
    """Helper to construct a Hand object from coordinate tuples."""
    lms = tuple(Landmark3D(x=x, y=y, z=z) for x, y, z in landmarks)
    return Hand(landmarks=lms, handedness=handedness, confidence=1.0)


